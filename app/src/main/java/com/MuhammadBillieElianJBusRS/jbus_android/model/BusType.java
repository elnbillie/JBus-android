package com.MuhammadBillieElianJBusRS.jbus_android.model;
/**
 * Enumerasi untuk berbagai jenis bus.
 * Enum ini menyediakan kategori untuk bus berdasarkan tipe dan konfigurasi mereka.
 */
public enum BusType {
    /**
     * Bus reguler, tipe bus standar dengan konfigurasi dasar.
     */
    REGULER,

    /**
     * Bus double decker, bus dengan dua tingkat, memberikan lebih banyak ruang dan kapasitas penumpang.
     */
    DOUBLE_DECKER,

    /**
     * Bus high decker, bus dengan tinggi lebih dari standar, biasanya memberikan ruang kaki lebih dan kenyamanan.
     */
    HIGH_DECKER,

    /**
     * Minibus, bus berukuran lebih kecil dengan kapasitas penumpang lebih sedikit, cocok untuk perjalanan pendek atau rute dengan penumpang lebih sedikit.
     */
    MINIBUS
}
