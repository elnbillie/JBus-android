package com.MuhammadBillieElianJBusRS.jbus_android.model;
/**
 * Kelas `Facility` merupakan enum yang mendefinisikan fasilitas-fasilitas yang tersedia pada bus.
 *
 * @author Muhammad Billie Elian J
 */
public enum Facility {

    /**
     * Akses internet nirkabel.
     */
    WIFI,

    /**
     * Pendingin ruangan.
     */
    AC,

    /**
     * Toilet.
     */
    TOILET,

    /**
     * Penyediaan makan siang.
     */
    LUNCH,

    /**
     * Ruang penyimpanan bagasi besar.
     */
    LARGE_BAGGAGE,

    /**
     * Soket listrik.
     */
    ELECTRIC_SOCKET,

    /**
     * Televisi LCD.
     */
    LCD_TV,

    /**
     * Kotak pendingin.
     */
    COOL_BOX

}
